class DataTypes{
    public static void main(String[] args){
        // INT
        int IntMinVal = Integer.MIN_VALUE;
        int IntMaxVal = Integer.MAX_VALUE;
        System.out.println("My minimum Int value : " +IntMinVal);
        System.out.println("My maximum Int value : " +IntMaxVal);
        // BYTE
        byte byteMinVal = Byte.MIN_VALUE;
        byte byteMaxVal = Byte.MAX_VALUE;
        System.out.println("My minimum Byte value : " +byteMinVal);
        System.out.println("My maximum Byte value : " +byteMaxVal);
        // SHORT
        short shortMinVal = Short.MIN_VALUE;
        short shortMaxVal = Short.MAX_VALUE;
        System.out.println("My minimum Short value : " +shortMinVal);
        System.out.println("My maximum Short value : " +shortMaxVal);
        // LONG
        long LongMinVal = Long.MIN_VALUE;
        long LongMaxVal = Long.MAX_VALUE;
        System.out.println("My minimum Long value : " +LongMinVal);
        System.out.println("My maximum Long value : " +LongMaxVal);
    }

}